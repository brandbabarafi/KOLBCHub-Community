// Peta cepat: kode provinsi -> nama provinsi.
export const PROVINCE_BY_CODE: Record<string, string> = Object.fromEntries(
  PROVINCES.map((p) => [p.code, p.name]),
);

// Cari nama provinsi dari nama kota.
export function provinceNameOfCity(cityName: string): string | null {
  const city = CITIES.find((c) => c.name === cityName);
  return city ? PROVINCE_BY_CODE[city.provinceCode] ?? null : null;
}

// Kota dikelompokkan per provinsi, untuk dropdown ber-optgroup.
export interface ProvinceGroup {
  code: string;
  name: string;
  cities: string[];
}

export const CITIES_BY_PROVINCE: ProvinceGroup[] = PROVINCES.map((p) => ({
  code: p.code,
  name: p.name,
  cities: CITIES.filter((c) => c.provinceCode === p.code).map((c) => c.name),
}));
